
public class Sprite {

	private int x;
	private int y;
	private int width;
	private int height;

	protected void setX(int x) {
		this.x = x;
	}

	protected void setY(int y) {
		this.y = y;
	}

	protected int getX() {
		return x;
	}

	protected int getY() {
		return y;

	}

	protected int getWidth() {
		return width;
	}

	protected void setWidth(int width) {
		this.width = width;
	}

	protected int getHeight() {
		return height;
	}

	protected void setHeight(int height) {
		this.height = height;
	}

}
